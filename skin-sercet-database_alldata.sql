CREATE TABLE COWH (
  COWH_NO int AUTO_INCREMENT,
  COMM_NO INT NOT NULL,
  PROC_NO INT NOT NULL,
  USER_NO CHAR(10) DEFAULT NULL,
  COWH_DATE DATE DEFAULT NULL,
  COWH_CONTENT LONGTEXT DEFAULT NULL,
  FOREIGN KEY (USER_NO) REFERENCES USER_NUMB (USER_NO),
  FOREIGN KEY (PROC_NO) REFERENCES PROC (PROC_NO),
  FOREIGN KEY (COMM_NO) REFERENCES COMM (COMM_NO),
  PRIMARY KEY (COWH_NO)
); 

